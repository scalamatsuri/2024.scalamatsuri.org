    <h3 style="font-size:2rem;margin-top: 40px;margin-bottom: 20px;">
      Carry cash
    </h3>
    <p>
      Japan is mostly cash-based country.
      With a few exceptions like hotels, nice restaurants, train stations, and some taxi, do not expect your credit card to be accepted.
      Even at a department store, foreign-issued credit cards may or may not work. Carry at least 10,000 JPY (around $100) at all times.
      Use your ATM card to withdraw Japanese Yen. First, <i>contact your bank in advance</i> to activate foreign withdrawal (otherwise they can suspend the card thinking it's hacked).
      There are <a href="https://www.citibank.co.jp/en/banking/branch_atm/" target="_blank">Citibank Japan</a>'s ATM machines at both Haneda and Narita airport.
      Once you are in Tokyo, look for a <a href="http://www.sej.co.jp/in/en.html" target="_blank">7 Eleven</a> (Japanese 7 Eleven is so badass they operate their own bank) or a <a href="http://www.jp-bank.japanpost.jp/en/ias/en_ias_index.html" target="_blank">Japan Post</a> (Japanese postal office use to operate one of the largest banks in the world).
    </p>

    <h3 style="font-size:2rem;margin-top: 40px;margin-bottom: 20px;">
      Buy Suica card
    </h3>
    <p>
      <a href="https://www.jreast.co.jp/e/pass/suica.html" target="_blank">Suica</a> is a prepaid IC card by JR East. Suica (and other compatible cards like Pasmo and Kitaca) can be used for almost all trains and subways in major cities.
      They can also be used for local bus lines, but we don't recommend you take buses since they are more involved, with the exception of the Airport Limousine bus (Unfortunately Suica card are accepted only on the inbound routes).
      In addition, the IC cards can be used at convenient stores, vending machines, and coin lockers especially around train stations.
    </p>

    <h3 style="font-size:2rem;margin-top: 40px;margin-bottom: 20px;">
      Power plugs
    </h3>
    <p>
      See <a href="http://www.japan-guide.com/e/e2225.html" target="_blank">Electricity</a> page.
      Japanese power outlets are identical to ungrounded (2-pin) North American outlets, but the voltage 100V, which is different from North America (120V), Central Europe (230V).
      Many laptop adapters including the ones for MacBook Pro work in terms of the voltage.
      Even if you are coming from North America, you might still need <a href="https://www.amazon.com/s?k=3-Prong-2-Prong-Electrical-Adapter&crid=3IDLHE5BBW2SK&sprefix=3-prong-2-prong-electrical-adapter+japan%2Caps%2C223&ref=nb_sb_noss" target="_blank">an adapter</a> to remove the earth pin from the extension cord.
    </p>

    <h3 style="font-size:2rem;margin-top: 40px;margin-bottom: 20px;">
      Tipping
    </h3>
    <p>
      In Japan, the service charges are included in the price. In other words, tipping is neither required nor accepted at hotels, restaurants, taxis etc.
      The only exception that you might come across is if you stay at a better ryokan style inn, where you are attended to by one person for your entire visit.
      The protocol is to put around 1000 JPY per guest in a small envelope (sold at a convenience store) and present to your attendant at the end of the in-room check-in process. This too is completely optional.
    </p>
    <p>
      At a restaurant, instead of tipping, a common etiquette is to thank the chef, the waitor, or the host as you walk out of the restaurant by bowing slightly and saying <a href="https://www.youtube.com/watch?v=I0Rp1W8-ZSM&t=1m16s" target="_blank"><b>ごちそうさまでした</b></a> (gochisousama-deshita), "thank you for the lovely meal."
      You can also say the same if someone buys you a meal.
    </p>

