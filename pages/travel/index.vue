      Casual dining
    </h3>
    <p>
      Beyond the usual sushi and tempura, there is an array of things you could try for more casual dining on the cheap.
      See <a href="http://www.japan-guide.com/e/e2036.html" target="_blank">Restaurants</a> page on japan-guide.com for details.
