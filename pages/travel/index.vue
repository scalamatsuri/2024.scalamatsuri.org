    <h3 style="font-size:2rem;margin-top: 40px;margin-bottom: 20px;">
      Coffee
    </h3>
    <p>
      The coffee quality can vary, but I generally find them weaker and sour in Japan.
      Thankfully there are <a href="https://maps.app.goo.gl/NMqL3ckUKQCTwJ156" target="_blank">Starbucks chains</a> in Tokyo (Blue Bottle Coffee too but not too many).
      Just type in <a href="https://www.google.com/maps/search/Starbucks+near+odaiba" target="_blank">"Starbucks near odaiba"</a> into Google Map.
    </p>

    <h3 style="font-size:2rem;margin-top: 40px;margin-bottom: 20px;">
      Bakery
    </h3>
    <p>
      Japanese style bakery sells interesting mix of sweet and savory pastries and bread that you won't find elsewhere.
      Try curry bread (カレーパン), mellon bread (メロンパン), bacon pain d'epi (ベーコンエピ), or anything you think looks interesting.
    </p>

    <h3 style="font-size:2rem;margin-top: 40px;margin-bottom: 20px;">
      Convenience store
    </h3>
    <p>
      <a href="http://www.japan-guide.com/e/e2071.html" target="_blank">Convenience stores</a>, many open 24 hours a day, sell a large range of meals, such as onigiri (rice balls), sandwiches, obento (lunch boxes), and hot foods like fried chicken. For (lacto-) vegetarians, the fact that they have bread, yogurt, and salad helps as the last resort if you can't find food.
    </p>

    <h3 style="font-size:2rem;margin-top: 40px;margin-bottom: 20px;">
      Soba
    </h3>
    <p>
      Eating a lunch at a local soba shop is a truely Japanese experience for common folks, usually more reasonable than other fancier cuisine. There's usually a daily special that combines soba and one more dish.
    </p>

    <h3 style="font-size:2rem;margin-top: 40px;margin-bottom: 20px;">
      Izakaya
    </h3>
    <p>
      <a href="http://www.japan-guide.com/e/e2036_izakaya.html" target="_blank">Izakaya</a> (居酒屋) are casual drinking establishments, similar to tapas bars, where customers order a variety of small dishes of food that can be shared at the table.
    </p>

    <h3 style="font-size:2rem;margin-top: 40px;margin-bottom: 20px;">
      Japanese curry
    </h3>
    <p>
      Curry rice is one of the most popular dish cooked in Japanese households. Compared to the Indian or Thai variety they are usually on more milder side.
    </p>

    <h3 style="font-size:2rem;margin-top: 40px;margin-bottom: 20px;">
      Ramen
    </h3>
    <p>
      If you're looking for a quick meal or to top off the night after drinking, <a href="http://www.japan-guide.com/e/e2042.html" target="_blank">ramen</a> could be what you want.
      <br/><br/>
      For everyone who wants to try a more powerful ramen, I highly recommend experiencing the exquisite ramen at Ramen Jiro(ラーメン二郎). Ramen Jiro is known for its unique and rich pork bone soup, thick and chewy curly noodles, and generously topped ingredients that create a flavorful and hearty experience.
      Each bowl of ramen comes loaded with a substantial amount of bean sprouts, cabbage, and pork belly, making for an enticing combination. Moreover, in the welcoming atmosphere of the restaurant, you can enjoy the distinctive ramen culture.
      As part of experiencing Japan's culinary culture, be sure to pay a visit. The ramen at Ramen Jiro is a unique and satisfying example of food that you won't find elsewhere.
      <br/>
      <a href="https://en.wikipedia.org/wiki/Ramen_Jiro" target="_blank">Ramen Jiro(Wikipedia)</a>
    </p>

    <h3 style="font-size:2rem;margin-top: 40px;margin-bottom: 20px;">
      Tonkatsu
    </h3>
    <p>
      <a href="http://www.japan-guide.com/e/e2339.html" target="_blank">Tonkatsu</a> (とんかつ, "pork cutlet") are thick slices of pork that are breaded and deep fried in a manner similar to German schnitzel. It's a popular dinner option for dining out.
    </p>

