  section5_title: Travel Grant
  section5_body: |
    ScalaMatsuri wants diverse speakers from all over the world. To help break down the barriers, we’re excited to offer travel grant.<br/>
    <br/>
    Because ScalaMatsuri is run by a non-profit association, the travel grant must only be used for something that we can file as an expense. Here are the categories that is ok:<br/>
    <br/>
    <ul class="section_note">
      <li class="section_note_item">Airfare and other transportation to Tokyo.</li>
      <li class="section_note_item">Other transportation during ScalaMatsuri.</li>
      <li class="section_note_item">Hotel fares during ScalaMatsuri sessions.</li>
    </ul><br/>
    Any other expenses, including ones incurred from sightseeing or hotel fares (priced per person in Japan) for accompanying travellers, cannot be expensed by us.<br/>
    <br/>
    We’ll accept expense requests after ScalaMatsuri for a month. All items must have either an email forward from an airline or scans/photos of a receipt. If you need earlier financial support, we will do our best to accomodate you.<br/>
    <br/>
    The maximum amount of the travel grant is fixed by the origin of your travel:<br/>
    <br/>
    <ul class="section_note">
      <li class="section_note_item">Within Asia: 500 USD</li>
      <li class="section_note_item">Outside Asia: 1000 USD</li>
    </ul><br/><br/>
    Note that the amount offered as travel grant will NOT exceed the actual travel expense.<br/>
    Suppose you’ve been approved for the grant and the travel expense was $1500 from Europe, the maximum amount the ScalaMatsuri will reimburse will be $1000. If you started from somewhere in Asia and it cost $700, the reimbursement will be $500.<br/><br/>
    Your expenses will NOT be accepted in the event that you do not attend the conference. At the sole discretion of ScalaMatsuri, some expenses may be reimbursed upon a natural disaster, death or illness of the speaker or a family member. We suggest you purchase travel insurance if these terms are not sufficient.
